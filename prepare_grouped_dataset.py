from __future__ import annotations

import argparse
import sys
from pathlib import Path

import matplotlib as mpl
import matplotlib.pyplot as plt
import pandas as pd
from matplotlib import font_manager
from matplotlib.ticker import MultipleLocator



CLASS_ORDER = [
    "喉癌",
    "声带息肉",
    "声带白斑",
    "声带炎",
    "正常声带",
    "声带沟",
]


ENGLISH_LABELS = {
    "喉癌": "Laryngeal carcinoma",
    "声带息肉": "Vocal fold polyp",
    "声带白斑": "Vocal fold leukoplakia",
    "声带炎": "Chorditis vocalis",
    "正常声带": "Normal vocal folds",
    "声带沟": "Sulcus vocalis",
}


EXPECTED_COUNTS = {
    "喉癌": 184,
    "声带息肉": 1246,
    "声带白斑": 198,
    "声带炎": 488,
    "正常声带": 496,
    "声带沟": 109,
}

EXPECTED_TOTAL = 2721


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Generate Figure 6 from the final grouped quality-control "
            "split_manifest.csv file."
        )
    )
    parser.add_argument(
        "--manifest",
        type=Path,
        default=Path(
            "grouped_quality_control/manifests/split_manifest.csv"
        ),
        help=(
            "Path to split_manifest.csv generated by "
            "prepare_grouped_dataset.py."
        ),
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("outputs"),
        help="Directory used to save PNG, PDF, SVG, and CSV outputs.",
    )
    parser.add_argument(
        "--skip-count-check",
        action="store_true",
    )
    return parser.parse_args()


def choose_font(candidates: list[str]) -> str:
    """Choose the first installed font from the candidate list."""
    installed = {font.name for font in font_manager.fontManager.ttflist}
    for candidate in candidates:
        if candidate in installed:
            return candidate
    return "DejaVu Serif"


def configure_matplotlib() -> None:
    """Configure publication-oriented Matplotlib settings."""
    font_name = choose_font(
        [
            "Times New Roman",
            "Liberation Serif",
            "Nimbus Roman",
            "DejaVu Serif",
        ]
    )

    mpl.rcParams.update(
        {
            "font.family": font_name,
            "font.size": 10.2,
            "axes.labelsize": 11.0,
            "xtick.labelsize": 9.4,
            "ytick.labelsize": 9.8,
            "axes.unicode_minus": False,
            "pdf.fonttype": 42,
            "ps.fonttype": 42,
            "svg.fonttype": "none",
            "savefig.bbox": "tight",
            "savefig.pad_inches": 0.06,
        }
    )


def read_manifest(manifest_path: Path) -> pd.DataFrame:
    """Read and validate the final split manifest."""
    manifest_path = manifest_path.expanduser().resolve()

    if not manifest_path.is_file():
        raise FileNotFoundError(
            f"Manifest file does not exist: {manifest_path}"
        )

    dataframe = pd.read_csv(
        manifest_path,
        encoding="utf-8-sig",
        keep_default_na=False,
    )

    required_columns = {"class_name", "group_id", "validation_fold"}
    missing_columns = sorted(required_columns - set(dataframe.columns))
    if missing_columns:
        raise ValueError(
            "split_manifest.csv is missing required columns: "
            f"{missing_columns}"
        )

    if dataframe.empty:
        raise ValueError("split_manifest.csv contains no image records.")

    dataframe = dataframe.copy()
    dataframe["class_name"] = (
        dataframe["class_name"].astype(str).str.strip()
    )

    unknown_classes = sorted(
        set(dataframe["class_name"]) - set(CLASS_ORDER)
    )
    if unknown_classes:
        raise ValueError(
            "Unexpected class names were found in split_manifest.csv: "
            f"{unknown_classes}"
        )

    missing_classes = [
        class_name
        for class_name in CLASS_ORDER
        if class_name not in set(dataframe["class_name"])
    ]
    if missing_classes:
        raise ValueError(
            "The following classes are missing from split_manifest.csv: "
            f"{missing_classes}"
        )

    folds_per_group = (
        dataframe.groupby("group_id")["validation_fold"].nunique()
    )
    if not folds_per_group.eq(1).all():
        bad_groups = folds_per_group[
            folds_per_group.ne(1)
        ].index.tolist()
        raise ValueError(
            "Group leakage was detected across validation folds. "
            f"First problematic groups: {bad_groups[:10]}"
        )

    return dataframe


def build_distribution(dataframe: pd.DataFrame) -> pd.DataFrame:
    counts = dataframe["class_name"].value_counts()

    records = []
    for class_name in CLASS_ORDER:
        count = int(counts.get(class_name, 0))
        records.append(
            {
                "class_name": class_name,
                "class": ENGLISH_LABELS[class_name],
                "count": count,
            }
        )

    distribution = pd.DataFrame(records)
    total = int(distribution["count"].sum())
    distribution["percentage"] = (
        distribution["count"] / total * 100.0
    )
    return distribution


def validate_expected_counts(
    distribution: pd.DataFrame,
    skip_count_check: bool,
) -> None:
    if skip_count_check:
        return

    observed = dict(
        zip(
            distribution["class_name"],
            distribution["count"].astype(int),
        )
    )
    total = int(distribution["count"].sum())

    if observed != EXPECTED_COUNTS or total != EXPECTED_TOTAL:
        raise ValueError(
            "The manifest counts do not match the expected final dataset.\n"
            f"Expected total: {EXPECTED_TOTAL}\n"
            f"Observed total: {total}\n"
            f"Expected counts: {EXPECTED_COUNTS}\n"
            f"Observed counts: {observed}\n"
            "If the dataset was intentionally updated, rerun with "
            "--skip-count-check and update the manuscript accordingly."
        )


def choose_major_tick(max_count: int) -> int:
    """Choose a readable x-axis major tick interval."""
    if max_count <= 300:
        return 50
    if max_count <= 800:
        return 100
    if max_count <= 1600:
        return 200
    return 500


def plot_figure(
    distribution: pd.DataFrame,
    output_dir: Path,
) -> None:
    configure_matplotlib()

    output_dir = output_dir.expanduser().resolve()
    output_dir.mkdir(parents=True, exist_ok=True)

    total = int(distribution["count"].sum())

    distribution.to_csv(
        output_dir
        / "figure6_class_distribution_data_2721.csv",
        index=False,
        encoding="utf-8-sig",
    )


    plot_data = distribution.iloc[::-1].reset_index(drop=True)

    fig, ax = plt.subplots(figsize=(7.35, 4.45))

    bars = ax.barh(
        plot_data["class"],
        plot_data["count"],
        height=0.66,
        edgecolor="none",
    )

    ax.set_xlabel("Number of images")
    ax.set_ylabel("")

    max_count = int(plot_data["count"].max())
    tick_interval = choose_major_tick(max_count)
    ax.xaxis.set_major_locator(MultipleLocator(tick_interval))

    ax.grid(
        True,
        axis="x",
        linewidth=0.45,
        alpha=0.12,
    )
    ax.set_axisbelow(True)

    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    ax.spines["left"].set_linewidth(0.75)
    ax.spines["bottom"].set_linewidth(0.75)

    ax.tick_params(
        axis="both",
        direction="out",
        length=3.2,
        width=0.7,
    )

    x_limit = max_count * 1.18
    ax.set_xlim(0, x_limit)

    annotation_offset = max_count * 0.012

    for bar, (_, row) in zip(bars, plot_data.iterrows()):
        count = int(row["count"])
        percentage = float(row["percentage"])
        x = float(count)
        y = bar.get_y() + bar.get_height() / 2

        ax.text(
            x + annotation_offset,
            y,
            f"{count} ({percentage:.2f}%)",
            va="center",
            ha="left",
            fontsize=9.0,
        )

    fig.savefig(
        output_dir
        / "figure6_class_distribution_final_2721.png",
        dpi=600,
    )
    fig.savefig(
        output_dir
        / "figure6_class_distribution_final_2721.pdf",
    )
    fig.savefig(
        output_dir
        / "figure6_class_distribution_final_2721.svg",
    )
    plt.close(fig)

    print("Figure 6 generated successfully.")
    print(f"Final modeling images: {total}")
    print("Class distribution:")
    for row in distribution.itertuples(index=False):
        print(
            f"  {row.class_name}: "
            f"{int(row.count)} ({float(row.percentage):.2f}%)"
        )
    print(f"Outputs: {output_dir}")


def main() -> int:
    args = parse_args()

    try:
        dataframe = read_manifest(args.manifest)
        distribution = build_distribution(dataframe)
        validate_expected_counts(
            distribution,
            skip_count_check=args.skip_count_check,
        )
        plot_figure(distribution, args.output_dir)
        return 0
    except Exception as exc:
        print(f"ERROR: {exc}", file=sys.stderr)
        return 1


if __name__ == "__main__":
    sys.exit(main())
